package de.kaiserpfalzedv.office.library.location;

import io.quarkus.hibernate.orm.panache.PanacheRepositoryBase;
import io.quarkus.panache.common.Parameters;

import javax.enterprise.context.ApplicationScoped;
import java.util.Optional;
import java.util.UUID;

@ApplicationScoped
public class LocationRepository implements PanacheRepositoryBase<MediumLocation, UUID> {

    public Optional<MediumLocation> findByName(final String name) {
        return MediumLocation.find("name = :name", Parameters.with("name", name))
                .singleResultOptional();
    }
}